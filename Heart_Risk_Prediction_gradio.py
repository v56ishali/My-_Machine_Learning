# Prediction function with strict check
def predict_risk(age, bp, chol):
    if age is None or bp is None or chol is None:
        return "âŒ Error: All three inputs (Age, Blood Pressure, Cholesterol) must be provided!"

    if not (0 <= age <= 120):
        return "âŒ Age must be between 0 and 120"
    if not (60 <= bp <= 200):
        return "âŒ Blood Pressure must be between 60 and 200"
    if not (100 <= chol <= 400):
        return "âŒ Cholesterol must be between 100 and 400"

    input_data = np.array([[age, bp, chol]])
    input_scaled = scaler.transform(input_data)
    cluster = kmeans.predict(input_scaled)[0]
    risk, quote = risk_labels.get(cluster, ("â“ Unknown", "Stay healthy and strong!"))
    return f"ğŸ©º Risk Level: {risk}\nğŸ’¬ Motivation: {quote}"

